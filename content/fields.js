export const typesEmit = {
  public: 'Общепит',
  trade: 'Торговля',
  services: 'Услуги'
}
export function getNameByType (type) {
  return typesEmit[type]
}
export function filterByType (type) {
  if (!type) { return getBaseData() }
  if (type === 'all') { return getBaseData() }
  return pages.filter(x => x.page.type === type)
}
export function getAllTypes () {
  return pages.map((x) => {
    return {
      type: x.page.type,
      type_ru: getNameByType(x.page.type)
    }
  })
}
export function getBaseData () {
  return pages.map((x) => {
    return {
      page: x.page,
      intro: x.intro
    }
  })
}
export function getPageParams () {
  return pages.map(element => element.page)
}

export function getPageByUrl (url) {
  return pages.find(element => element?.page?.url === url)
}
